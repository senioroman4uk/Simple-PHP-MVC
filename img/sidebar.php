                    DETROIT -- Google Inc. and the auto industry are closer friends than one might think.
                    Chris Urmson, the director of Google's autonomous driving project, revealed at the
                    Automotive News World Congress last week that Google is working closely with global
                    suppliers such as Bosch and Continental to build the prototypes that it plans to begin
                    testing on California roads this year.
                    He said Google also has initiated talks with most of the world's largest automakers
                    to prepare for the day when Google sees mass production of self-driving cars for
                    commercial uses. Urmson has set a goal of putting such a car on the road by the
                    time his 11-year-old son starts driving -- likely around 2020.
